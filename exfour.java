package lab5;
public class exfour {


public static void main(String[] args)
{
	System.out.print(" ======The Fourth Pattern=====");
	System.out.println();
	int i,j;
	for(i=1; i<6;i++)
	{
		System.out.println();
		for(j=1;j<=i;j++)
			System.out.print(j+" ");
		
	}
	System.out.println();
	for(i=4;i>0;i--)
	{
		for(j=1;j<=i;j++)
			System.out.print(j+" ");
		System.out.println();
		
	}
	}
}
