package lab5;

public class exfive {
	public static void main(String[] args)
	{
		System.out.print(" ======The Fifth Pattern=====");
		System.out.println();
		int i,j;
		for(i=5;i>0;i--)
		{
			for(j=1;j<=i;j++)
				System.out.print(j+" ");
			System.out.println();
			
		}
		for(i=1; i<6;i++)
		{
			for(j=1;j<=i;j++)
				System.out.print(j+" ");
			System.out.println();
			
		}

}
}

