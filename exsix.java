package lab5;

public class exsix {
	public static void main(String[] args)
	{
		System.out.println(" ======The Sixth Pattern=====");
		int i,j;
		for(i=5;i>0;i--)
		{
			for(j=i;j>0;j--)
				System.out.print(j+" ");
			System.out.println();
			
		}
		for(i=1; i<6;i++)
		{
			for(j=i;j>0;j--)
				System.out.print(j+" ");
			System.out.println();
			
		}
	}
}
